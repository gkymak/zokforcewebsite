// Language Manager Class
class LanguageManager {
    constructor() {
        this.supportedLanguages = ['en', 'zh-TW', 'zh-CN'];
        this.currentLanguage = this.detectLanguage();
        this.init();
    }

    detectLanguage() {
        // Check URL parameter first
        const urlParams = new URLSearchParams(window.location.search);
        const langParam = urlParams.get('lang');
        if (langParam && this.supportedLanguages.includes(langParam)) {
            return langParam;
        }

        // Check localStorage
        const savedLang = localStorage.getItem('zokforce-language');
        if (savedLang && this.supportedLanguages.includes(savedLang)) {
            return savedLang;
        }

        // Check browser language
        const browserLang = navigator.language || navigator.userLanguage;
        if (browserLang.startsWith('zh')) {
            if (browserLang.includes('TW') || browserLang.includes('HK') || browserLang.includes('MO')) {
                return 'zh-TW';
            } else {
                return 'zh-CN';
            }
        }

        // Default to English
        return 'en';
    }

    init() {
        this.updatePageLanguage();
        this.createLanguageSelector();
        this.updateURL();
    }

    translate(key, fallback = '') {
        const keys = key.split('.');
        let value = translations;
        
        for (const k of keys) {
            if (value && typeof value === 'object' && k in value) {
                value = value[k];
            } else {
                return fallback || key;
            }
        }
        
        return value[this.currentLanguage] || value['en'] || fallback || key;
    }

    setLanguage(lang) {
        if (this.supportedLanguages.includes(lang)) {
            this.currentLanguage = lang;
            localStorage.setItem('zokforce-language', lang);
            this.updatePageLanguage();
            this.updateURL();
            this.updateActiveLanguageButton(lang);
        }
    }

    updateActiveLanguageButton(lang) {
        // Remove active class from all buttons
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
        });
        
        // Add active class to current language button
        const activeButton = document.querySelector(`.lang-btn[data-lang="${lang}"]`);
        if (activeButton) {
            activeButton.classList.add('active');
        }
    }

    updateURL() {
        const url = new URL(window.location);
        url.searchParams.set('lang', this.currentLanguage);
        window.history.replaceState({}, '', url);
    }

    updatePageLanguage() {
        // Update document language
        document.documentElement.lang = this.currentLanguage;
        
        // Update meta tags
        document.title = this.translate('meta.title');
        const metaDescription = document.querySelector('meta[name="description"]');
        if (metaDescription) {
            metaDescription.content = this.translate('meta.description');
        }

        // Update all translatable elements
        this.updateTranslatableElements();
    }

    updateTranslatableElements() {
        // Update elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.translate(key);
            if (translation !== key) {
                element.innerHTML = translation;
            }
        });

        // Update placeholder attributes
        document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            element.placeholder = this.translate(key);
        });

        // Update label attributes
        document.querySelectorAll('[data-translate-label]').forEach(element => {
            const key = element.getAttribute('data-translate-label');
            element.setAttribute('aria-label', this.translate(key));
        });
    }

    createLanguageSelector() {
        // This method can be implemented if needed for dynamic language selector creation
        return;
    }

    setupLanguageSelectorEvents() {
        const toggle = document.getElementById('language-toggle');
        const dropdown = document.getElementById('language-dropdown');
        const options = document.querySelectorAll('.language-option');

        // Toggle dropdown
        if (toggle && dropdown) {
            toggle.addEventListener('click', (e) => {
                e.stopPropagation();
                dropdown.classList.toggle('show');
            });

            // Close dropdown when clicking outside
            document.addEventListener('click', () => {
                dropdown.classList.remove('show');
            });

            dropdown.addEventListener('click', (e) => {
                e.stopPropagation();
            });
        }

        // Handle language selection
        options.forEach(option => {
            option.addEventListener('click', (e) => {
                e.preventDefault();
                const lang = option.getAttribute('data-lang');
                if (lang) {
                    this.setLanguage(lang);
                    dropdown.classList.remove('show');
                    
                    // Update active state
                    options.forEach(opt => opt.classList.remove('active'));
                    option.classList.add('active');
                }
            });
        });
    }
}

// Initialize language manager when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    window.languageManager = new LanguageManager();
    window.languageManager.init();
});

// Global function for language change with file routing
function changeLanguage(lang) {
    // Check if we're on a blog post page that needs routing
    const currentPath = window.location.pathname;
    const isBlogPost = currentPath.includes('implementing-llms-practical-guide-zokforce');
    
    if (isBlogPost) {
        // First, update the current page translations (header/footer)
        if (window.languageManager) {
            window.languageManager.setLanguage(lang);
        }
        
        // Then handle blog post language routing after a short delay
        setTimeout(() => {
            let targetFile;
            switch(lang) {
                case 'zh-CN':
                    targetFile = 'implementing-llms-practical-guide-zokforce-zh-CN.html';
                    break;
                case 'zh-TW':
                    targetFile = 'implementing-llms-practical-guide-zokforce-zh-TW.html';
                    break;
                case 'en':
                default:
                    targetFile = 'implementing-llms-practical-guide-zokforce.html';
                    break;
            }
            
            // Get the current directory path
            const pathParts = currentPath.split('/');
            pathParts[pathParts.length - 1] = targetFile; // Replace the filename
            const newPath = pathParts.join('/');
            
            // Redirect to the target file
            window.location.href = newPath;
        }, 100); // Small delay to allow translation update to be visible
    } else {
        // For other pages, use the original language manager
        if (window.languageManager) {
            window.languageManager.setLanguage(lang);
        }
    }
}

// Export for use in other modules
window.changeLanguage = changeLanguage;

